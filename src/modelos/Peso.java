public class Peso {
    private double peso1;
    private double peso2;

    public Peso() {
    }

    public Peso(double peso1, double peso2) {
        this.peso1 = peso1;
        this.peso2 = peso2;
    }

    public double getPeso1() {
        return peso1;
    }

    public void setPeso1(double peso1) {
        this.peso1 = peso1;
    }

    public double getPeso2() {
        return peso2;
    }

    public void setPeso2(double peso2) {
        this.peso2 = peso2;
    }
}
