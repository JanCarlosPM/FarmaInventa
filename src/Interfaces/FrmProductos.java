    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        DProductosImplementacion productos_dao = new DProductosImplementacion();
        Productos productos = new Productos();

        if (txtProducto.getText().equals("") || txtDescripcion.getText().equals("") || txtPrecio.getText().equals("")
                || txtStock.getText().equals("") || txtFecha_vencimiento.getText().equals("") || txtFecha_registro.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por Favor Rellene todos los Campos", "Farmacia", 2);
        } else {
            
            if (txtFecha_vencimiento.getText().length() != 7) {

                JOptionPane.showMessageDialog(null, "El formato de la fecha de vencimiento es incorrecto, asegurese de\n"
                        + " seguir el formato valido\n"
                        + " ej: 2022-08", "Farmacia", 2);
            } else {
                
                String producto = txtProducto.getText();
                String descripcion = txtDescripcion.getText();
                String precio = txtPrecio.getText();
                String stock = txtStock.getText();
                String fecha_vencimiento = txtFecha_vencimiento.getText();
                String fecha_registro = txtFecha_registro.getText();

                productos.setProducto(producto);
                productos.setDescripcion(descripcion);
                productos.setPrecio(Float.parseFloat(precio));
                productos.setStock(stock);
                productos.setFecha_vencimiento(fecha_vencimiento);
                productos.setFecha_registro(fecha_registro);

                productos_dao.registrarProducto(productos);
                limpiar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed
