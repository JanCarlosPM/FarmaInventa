    public static boolean esSegura(String fecha) {
        int specialCounter = 0;
        final int Special = 1;

        for (int i = 0; i < fecha.length(); i++) {

            char c = fecha.charAt(i);

            if (c >= 33 && c <= 46 || c == 64) {
                specialCounter++;
            }
        }

        if (specialCounter >= Special) {
            return true;
        } else {
            return false;
        }
    }

